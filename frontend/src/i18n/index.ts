function normalizeLocale(raw: string | null | undefined): Locale {
  if (!raw) return 'en-US';
  return raw.toLowerCase().startsWith('zh') ? 'zh-CN' : 'en-US';
}

export function getLocale(): Locale {
  if (typeof window === 'undefined') return 'en-US';
  const saved = window.localStorage.getItem(LOCALE_KEY);
  if (saved) return normalizeLocale(saved);
  return normalizeLocale(window.navigator.language);
}

export function setLocale(locale: Locale) {
  if (typeof window === 'undefined') return;
  window.localStorage.setItem(LOCALE_KEY, locale);
}

export function tByLocale(locale: Locale, key: string): string {
  return messages[locale][key] ?? messages['en-US'][key] ?? key;
}

export function t(key: string): string {
  return tByLocale(getLocale(), key);
}
